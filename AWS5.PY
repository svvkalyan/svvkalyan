base = 2
exponent = 3
result = base ** exponent
print(result)  # Output: 8


# Left Shift
num = 10  # Binary: 1010
shifted_left = num << 2  # Binary: 101000 (40 in decimal)
print(shifted_left)  # Output: 40

# Right Shift
num = 40  # Binary: 101000
shifted_right = num >> 2  # Binary: 1010 (10 in decimal)
print(shifted_right)  # Output: 10




num1 = 15  # Binary: 1111
num2 = 7   # Binary: 0111
result = num1 & num2  # Binary: 0111 (7 in decimal)
print(result)  # Output: 7





x = True
y = False
result = x and y
print(result)  # Output: False